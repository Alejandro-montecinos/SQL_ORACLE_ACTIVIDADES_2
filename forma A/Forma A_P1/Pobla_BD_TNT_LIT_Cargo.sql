ALTER TABLE recinto ADD CONSTRAINT recinto_pk PRIMARY KEY ( id_recinto );

CREATE TABLE tracto_camion (
    patente_tracto_camion       VARCHAR2(7) NOT NULL,
    marca_tracto_camion         VARCHAR2(50) NOT NULL,
    modelo_tracto_camion        VARCHAR2(50),

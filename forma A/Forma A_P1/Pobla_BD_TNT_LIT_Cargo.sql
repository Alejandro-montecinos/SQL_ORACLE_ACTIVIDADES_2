ALTER TABLE camion ADD CONSTRAINT camion_pk PRIMARY KEY ( patente_camion );

CREATE TABLE ciudad (
    id_ciudad     NUMBER NOT NULL,
    nombre_ciudad VARCHAR2(50) NOT NULL,
    id_pais       NUMBER NOT NULL

ALTER TABLE conductor ADD CONSTRAINT conductor_pk PRIMARY KEY ( run_conductor );

CREATE TABLE empresa_cliente (
    id_empresa     NUMBER NOT NULL,
    nombre_empresa VARCHAR2(100) NOT NULL

                                                            id_turno );

CREATE TABLE camion (
    patente_camion          VARCHAR2(7) NOT NULL,
    marca_camion            VARCHAR2(50) NOT NULL,
    modelo_camion           VARCHAR2(50),
    cantidad_ejes_camion    NUMBER NOT NULL,

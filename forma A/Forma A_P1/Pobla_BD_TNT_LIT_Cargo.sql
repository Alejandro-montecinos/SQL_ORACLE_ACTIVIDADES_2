ALTER TABLE comuna ADD CONSTRAINT comuna_pk PRIMARY KEY ( id_comuna );

CREATE TABLE conductor (
    run_conductor       NUMBER NOT NULL,
    dvrun_conductor     VARCHAR2(1),
    pnombre_conductor   VARCHAR2(30) NOT NULL,
    snombre_conductor   VARCHAR2(30),
    appaterno_conductor VARCHAR2(30),

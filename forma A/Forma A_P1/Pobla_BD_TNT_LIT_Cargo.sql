ALTER TABLE tracto_camion ADD CONSTRAINT tracto_camion_pk PRIMARY KEY ( patente_tracto_camion );

CREATE TABLE turno (
    id_turno        NUMBER NOT NULL,
    fecha_ini_turno DATE NOT NULL,
    fecha_ter_turno DATE NOT NULL,
    horario_turno   VARCHAR2(20) NOT NULL,
    valor_turno     NUMBER

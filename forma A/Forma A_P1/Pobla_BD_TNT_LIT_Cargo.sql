ALTER TABLE guardia ADD CONSTRAINT guardia_pk PRIMARY KEY ( id_guardia );

CREATE TABLE pais (
    id_pais     NUMBER NOT NULL,
    nombre_pais VARCHAR2(50) NOT NULL
);

ALTER TABLE pais ADD CONSTRAINT pais_pk PRIMARY KEY ( id_pais );

CREATE TABLE recinto (
    id_recinto                 NUMBER NOT NULL,
    nombre_recinto             VARCHAR2(50) NOT NULL,
    direccion_recinto          VARCHAR2(100) NOT NULL,
    id_empresa                 NUMBER NOT NULL,
    id_comuna                  NUMBER
